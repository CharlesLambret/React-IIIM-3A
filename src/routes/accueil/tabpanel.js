import * as React from 'react';
import PropTypes from 'prop-types';
import Tabs from '@mui/material/Tabs';
import Tab from '@mui/material/Tab';
import Typography from '@mui/material/Typography';
import Box from '@mui/material/Box';
import Kanban from './views/kanban';
import TaskCard from '../../composants/cards/task';
import TaskTable from './views/table';

function TabPanel(props) {
  const { children, value, index, ...other } = props;

  return (
    <div
      role="tabpanel"
      hidden={value !== index}
      id={`simple-tabpanel-${index}`}
      aria-labelledby={`simple-tab-${index}`}
      {...other}
    >
      {value === index && (
        <Box sx={{ p: 3 }}>
          <Typography>{children}</Typography>
        </Box>
      )}
    </div>
  );
}

TabPanel.propTypes = {
  children: PropTypes.node,
  index: PropTypes.number.isRequired,
  value: PropTypes.number.isRequired,
};

function a11yProps(index) {
  return {
    id: `simple-tab-${index}`,
    'aria-controls': `simple-tabpanel-${index}`,
  };
}

export default function BasicTabs() {
  const [value, setValue] = React.useState(0);

  const handleChange = (event, newValue) => {
    setValue(newValue);
  };
  const ShowCreateTableButton = () => {}
  return (
    <Box variant="fullWidth" id ="TabBox" centered>
      <Box sx={{ borderBottom: 1, borderColor: 'divider' }}>
        <Tabs value={value} onChange={handleChange} aria-label="basic tabs example">
          <Tab label="Kanban" {...a11yProps(0)} />
          <Tab label="Tableau" {...a11yProps(1)} />
          
        </Tabs>
      </Box>
      <TabPanel value={value} index={0} >
        <Kanban/>
      </TabPanel>
      <TabPanel value={value} index={1} onClick={}>
        <TaskTable/>
      </TabPanel>
    </Box>
  );
}